    public static int getFlagResource(String currencyCode) {
        // Try to get the flag for the requested currency
        Integer flagRes = FLAG_RESOURCES.get(
                currencyCode != null ? currencyCode.toUpperCase(Locale.ROOT) : null
        );

        // If not found, use DEFAULT
        if (flagRes == null) {
            flagRes = FLAG_RESOURCES.get("DEFAULT");
        }

        // Make sure we don't convert null to intâ€”use 0 if DEFAULT isn't set
        return (flagRes != null) ? flagRes : 0;
    }
